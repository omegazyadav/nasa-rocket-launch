export default (id) => {
  switch (id) {
    case 1:
      return 'Launch is GO'
      break;
    case 2:
      return 'Launch is NO-GO'
      break;
    case 3:
      return 'Launch was a success'
      break;
    case 4:
      return 'Launch failed'
      break;
    case 5:
      return 'Unplanned hold'
      break;
    case 6:
      return 'Vehicle is in flight'
      break;
    case 7:
      return 'Partial Failure'
      break;
    default:

  }
}
